# Ejemplo 1
edades = [18, 25, 12, 30, 20]
edades.sort()
print(f"Edades ordenadas ascendente: {edades}")

# Ejemplo 2
notas = [3.5, 4.2, 2.8, 5.0, 4.0]
notas.sort(reverse=True)
print(f"Notas ordenadas descendente: {notas}")

# Ejemplo 3
numeros = [100, 50, 75, 25, 10]
numeros.sort()
print(f"Números ordenados: {numeros}")

# Ejemplo 4
temperaturas = [22, 30, 18, 25, 27]
temperaturas.sort(reverse=True)
print(f"Temperaturas de mayor a menor: {temperaturas}")

# Ejemplo 5
puntajes = [200, 150, 300, 100, 250]
puntajes.sort()
print(f"Puntajes ordenados: {puntajes}")